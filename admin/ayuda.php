    <h3><a href="#"><span class="iconfa-chevron-down"></span> ¿Cómo Crear una Cuenta de Usuario Nueva ?</a></h3>
        <div>
            <p>
                En el menu lateral izquierdo, ubique la opción "Cuentas" al pulsarla
                se desplegará un sub menú, seguidamente pulse "Nueva Cuenta" se mostrará
                un formulario para introducir los datos del usuario y la empresa asociada.
            </p>
        </div>
    <h3><a href="#"><span class="iconfa-chevron-down"></span> ¿Cómo modifico los datos de una cuenta?</a></h3>
        <div>
            <p>
                En el menu lateral izquierdo, ubique la opción "Cuentas" al pulsarla
                se desplegará un sub menú "Administrar Cuentas", al pulsar ésa opción se mostrarán los datos de todas las cuentas asociadas, ubique el panel "opciones" en la parte derecha
                de la fila que contiene los datos de la cuenta a modificar, pulse el icono de editar <i class="icon-edit"></i>, a continuación modifique los datos necesarios.
            </p>
        </div>
    <h3><a href="#"><span class="iconfa-chevron-down"></span> ¿Cómo elimino una cuenta del sistema?</a></h3>
        <div>
            <p>
